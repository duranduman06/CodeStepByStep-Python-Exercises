"""
Write a function named is_vowel that returns whether a string is a vowel (a single-letter string containing a, e, i, o, or u, case-insensitively).
"""

def is_vowel(letter):
    return (letter=="a" or letter=="e" or letter=="i" or letter=="o" or letter=="u" or letter=="A" or letter=="E" or letter=="I" or letter=="O" or letter=="U")
